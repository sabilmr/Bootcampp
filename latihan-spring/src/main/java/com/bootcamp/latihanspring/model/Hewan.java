package com.bootcamp.latihanspring.model;

public class Hewan {
    private String nameHewan;
    private int kakiHewan;
    private String makananHewan;
    private String berkembangbiak;
    private String kehidupanHewan;

    public String getNameHewan() {
        return nameHewan;
    }

    public void setNameHewan(String nameHewan) {
        this.nameHewan = nameHewan;
    }

    public int getKakiHewan() {
        return kakiHewan;
    }

    public void setKakiHewan(int kakiHewan) {
        this.kakiHewan = kakiHewan;
    }

    public String getMakananHewan() {
        return makananHewan;
    }

    public void setMakananHewan(String makananHewan) {
        this.makananHewan = makananHewan;
    }

    public String getBerkembangbiak() {
        return berkembangbiak;
    }

    public void setBerkembangbiak(String berkembangbiak) {
        this.berkembangbiak = berkembangbiak;
    }

    public String getKehidupanHewan() {
        return kehidupanHewan;
    }

    public void setKehidupanHewan(String kehidupanHewan) {
        this.kehidupanHewan = kehidupanHewan;
    }
}
